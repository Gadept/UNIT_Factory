#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		n < 0 ? write(fd, "-", 1) : 0;
		n < 0 ? (n = -n) : 0;
		n >= 10 ? ft_putnbr_fd(n / 10, fd) : 0;
		n >= 10 ? ft_putnbr_fd(n % 10, fd) : 0;
		if (n < 10)
		{
			n += 48;
			write(fd, &n, 1);
		}
	}
}
