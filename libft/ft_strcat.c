#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	t_var	v;

	if (!s1)
		return (0);
	if (s2)
	{
		v.i = ft_strlen(s1);
		v.j = 0;
		while (s2[v.j])
			s1[v.i++] = s2[v.j++];
		s1[v.i] = '\0';
	}
	return (s1);
}
