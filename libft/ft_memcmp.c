#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = -1;
	while (s1 && s2 && ++i != n)
		if ((((t_uc*)s1)[i] - ((t_uc*)s2)[i]))
			return ((((t_uc*)s1)[i] - ((t_uc*)s2)[i]));
	return (0);
}
