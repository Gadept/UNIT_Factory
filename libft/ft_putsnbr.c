#include "libft.h"

void	ft_putsnbr(long n)
{
	if (n == (-9223372036854775807 - 1))
		write(1, "-9223372036854775808", 20);
	else
	{
		n < 0 ? write(1, "-", 1) : 0;
		n < 0 ? (n = -n) : 0;
		n >= 10 ? ft_putsnbr(n / 10) : 0;
		n >= 10 ? ft_putsnbr(n % 10) : 0;
		if (n < 10)
		{
			n += 48;
			write(1, &n, 1);
		}
	}
}
