#include "libft.h"

void	ft_putunbr(t_ul n)
{
	n >= 10 ? ft_putunbr(n / 10) : 0;
	n >= 10 ? ft_putunbr(n % 10) : 0;
	if (n < 10)
	{
		n += 48;
		write(1, &n, 1);
	}
}
