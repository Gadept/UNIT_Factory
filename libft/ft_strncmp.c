#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = -1;
	while ((s1 && s2) && (size_t)++i < n)
	{
		if (s1[i] != s2[i])
			return ((t_uc)s1[i] - (t_uc)s2[i]);
		(!s1[i] && !s2[i]) ? (i = n) : 0;
	}
	return (0);
}
