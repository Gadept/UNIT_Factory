#include "libft.h"

char	*ft_itoa(int n)
{
	t_var	v;

	(n < 0) ? (v.d = -n) : 0;
	(n >= 0) ? (v.d = n) : 0;
	v.a = ft_nbrlen(v.d);
	(n < 0) ? (v.a++) : 0;
	v.str = ft_strnew(v.a);
	while (v.str && v.d)
	{
		v.str[--v.a] = (v.d % 10) + 48;
		v.d /= 10;
	}
	(n < 0) && (v.str) ? (v.str[--v.a] = 45) : 0;
	(!n) && (v.str) ? (v.str[--v.a] = 48) : 0;
	return (v.str);
}
