#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	if (!s1 && s2)
		return (ft_strdup(s2));
	else if (s1 && !s2)
		return (ft_strdup(s1));
	else
		return (ft_strcat(ft_strcpy(ft_strnew(ft_strlen(s1) +
											ft_strlen(s2)), s1), s2));
}
