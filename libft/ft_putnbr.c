#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		n < 0 ? write(1, "-", 1) : 0;
		n < 0 ? (n = -n) : 0;
		n >= 10 ? ft_putnbr(n / 10) : 0;
		n >= 10 ? ft_putnbr(n % 10) : 0;
		if (n < 10)
		{
			n += 48;
			write(1, &n, 1);
		}
	}
}
