	z[0] = '[';
	(v->k <= 9) ? (z[1] = v->k + 48) : 0;
	(v->k <= 9) ? (z[2] = ']') : 0;
	(v->k <= 9) ? (z[3] = 0) : 0;
	(v->k > 9) ? (z[1] = (v->k / 10) + 48) : 0;
	(v->k > 9) ? (z[2] = (v->k % 10) + 48) : 0;
